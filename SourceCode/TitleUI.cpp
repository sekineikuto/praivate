//-------------------------------------------------------------------------------------------------------------
// 2DUiの取得
//-------------------------------------------------------------------------------------------------------------
CUi2d * CTitleUI::Get2dui(int nIndex)
{
#ifdef _DEBUG
	if (CTitleUI::TYPE_NONE >= nIndex &&nIndex >= CTitleUI::TYPE_MAX)
	{
		return nullptr;
	}
#endif
	return m_2dui[nIndex];
}

//-------------------------------------------------------------------------------------------------------------
// 1行ずつ読み込む
//-------------------------------------------------------------------------------------------------------------
void CTitleUI::ReadFromLine(char* line, std::vector<CUi2d*> *p2dui)
{
	if (strcmp(line, "SET_UI") == 0)
	{
		m_bSetingUI = true;
	}
	else
	{// パラメータを設定
		ReadFromLineSetParam(line, p2dui);
	}
}

