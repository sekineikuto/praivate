#include "mode.h"

//-------------------------------------------------------------------------------------------------------------
// 初期化
//-------------------------------------------------------------------------------------------------------------
void CMode::Init(STATE State, int nMaxCnt)
{
	m_State        = State;
	m_nMaxCntState = nMaxCnt;
	m_nCntState    = MYLIB_INT_UNSET;
}

//-------------------------------------------------------------------------------------------------------------
// 更新
//-------------------------------------------------------------------------------------------------------------
void CMode::Update(void)
{
}

//-------------------------------------------------------------------------------------------------------------
// 状態の変更
//-------------------------------------------------------------------------------------------------------------
bool CMode::ChangeState(STATE State)
{
	if (CMylibrary::Setdifferentvalue(m_State, State) == true)
	{
		m_nCntState = MYLIB_INT_UNSET;
		return true;
	}

	return false;
}
